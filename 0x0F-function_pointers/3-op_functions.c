#include "3-calc.h"
/**
 * op_add - The sum of two integers
 * @a: first integer
 * @b: second integer
 * Return: The sum int
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - The difference between two integers
 * @a: first integer
 * @b: second integer
 * Return: The difference int
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - The product of two int
 * @a: first integer
 * @b: second integer
 * Return: The product int
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two int
 * @a: first integer
 * @b: second integer
 * Return: The result int when divided
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Finds the reamainder when divided
 * @a: first integer
 * @b: second integer
 * Return: The remainder int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
